// Variables
var HelloMessage: String  // 変数を宣言
HelloMessage = "Hello, world!" // 変数に代入
print(HelloMessage)



var HelloGreet: String = "Hello, Japan!" //変数に初期値をセット
print(HelloGreet)



var HelloSwift = "Hello, Swift!" // 初期値を代入して型宣言を省略
print(HelloSwift)



